'use strict';
///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////  CONSTRUCTOR
function Scores($resource, Config)
{
    var methods = {};

    methods.getScore = function()
    {
        return $resource( Config.ENV.server_url + '/scores/:locale/:from', {
        }, {
            update: {
                method: 'GET'
            }
        });
    };

    return methods;
}
///////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////   INIT MAP
///////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////// ANGULAR REGISTERING
Scores.$inject = ['$resource', 'Config'];
angular.module('game').factory('Scores', Scores);
